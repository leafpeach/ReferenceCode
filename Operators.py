# Arithmetic operators

print(3+2) #Addition
print(3-2) #Subtraction
print(3*2) #Multiplication
print(3/2) #Division
print(3%2) #Modulus (returns the remainder)
print(3**2) #Exponentiaton
print(3//2) #Floor division (returns the nearest whole number)

