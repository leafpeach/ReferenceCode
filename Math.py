#The min() and max() function

x = min(5,10,25)
y = max(5,10,25)

print(x, y)


#The absolute Value
z = abs(-69)

print(z)

#Power
power = pow(4, 6)
print(power)

#Built-in math module

import math

squareRoot = math.sqrt(64)

print(squareRoot)

ceiling = math.ceil(1.4) #returns 2
floor = math.floor(1.4) #returns 1
print(ceiling, floor)

what_is_pi = math.pi

print(what_is_pi)
